-- ============================================================================
-- 2. item_nameからcat01プレフィックスを削除
-- ============================================================================
-- WHERE句: item_nameがcat01の値 + '_'で始まる場合のみ処理
-- SUBSTR: cat01の長さ + 1文字（'_'）の次の文字から末尾までを取得

UPDATE estat_ranking_mappings
SET item_name = SUBSTR(item_name, LENGTH(cat01) + 2),
    updated_at = CURRENT_TIMESTAMP
WHERE item_name LIKE cat01 || '_%';

