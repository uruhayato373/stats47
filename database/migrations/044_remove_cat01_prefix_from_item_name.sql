--       cat01と同じ値のプレフィックス（例: A1101_、#M0330102_）を削除します。
--       例: 'A1101_総人口' → '総人口'

-- ============================================================================
-- 1. 更新前の確認クエリ（コメントアウト）
-- ============================================================================
-- 影響を受けるレコード数を確認する場合は、以下のクエリを実行してください:
