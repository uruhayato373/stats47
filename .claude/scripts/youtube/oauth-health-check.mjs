#!/usr/bin/env node
/**
 * YouTube OAuth refresh token の週次 health check。
 *
 * GitHub Actions `.github/workflows/oauth-token-health-check.yml` から呼ばれる。
 * `youtube.channels.list({ mine: true, part: ['id'] })` を 1 回叩いて token の生存確認をする。
 *   - 成功: 無音終了（exit 0）
 *   - invalid_grant: `[OAuth Alert] YouTube refresh token expired` Issue を起票（既に open ならスキップ）して exit 0
 *   - その他のエラー: exit 1（workflow を failure にする）
 *
 * Usage:
 *   node .claude/scripts/youtube/oauth-health-check.mjs
 *   node .claude/scripts/youtube/oauth-health-check.mjs --dry-run  # Issue 起票しない
 */

import { google } from "googleapis";
import { readFileSync, writeFileSync, existsSync } from "node:fs";
import { spawnSync } from "node:child_process";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const PROJECT_ROOT = resolve(
  dirname(fileURLToPath(import.meta.url)),
  "..",
  "..",
  "..",
);
const ENV_PATH = resolve(PROJECT_ROOT, ".env.local");
const DRY_RUN = process.argv.includes("--dry-run");

function log(msg) {
  console.error(`[oauth-health-check] ${msg}`);
}

function loadEnv() {
  // GitHub Actions では .env.local を workflow が compose する前提。
  // ローカル実行時は同じパスから読む。
  if (!existsSync(ENV_PATH)) {
    log(`.env.local not found at ${ENV_PATH}`);
    process.exit(1);
  }
  const env = {};
  for (const line of readFileSync(ENV_PATH, "utf-8").split("\n")) {
    const m = line.match(/^([A-Z_]+)=(.*)$/);
    if (m) env[m[1]] = m[2];
  }
  return env;
}

function runGh(args) {
  const res = spawnSync("gh", args, {
    encoding: "utf-8",
    maxBuffer: 5 * 1024 * 1024,
  });
  if (res.status !== 0) {
    log(`gh ${args.join(" ")} failed: ${res.stderr}`);
    return null;
  }
  return res.stdout;
}

function findOpenAlertIssue() {
  const raw = runGh([
    "issue",
    "list",
    "--state",
    "open",
    "--label",
    "oauth-alert",
    "--json",
    "number,title",
    "--limit",
    "5",
  ]);
  if (!raw) return null;
  try {
    const issues = JSON.parse(raw);
    return issues.find((i) => i.title.includes("YouTube refresh token")) || null;
  } catch {
    return null;
  }
}

function createAlertIssue(errorMessage) {
  const today = new Date(Date.now() + 9 * 60 * 60 * 1000)
    .toISOString()
    .slice(0, 10);
  const title = `[OAuth Alert] YouTube refresh token expired (${today})`;
  const body = `Weekly OAuth health check detected token expiration.

## Error
\`\`\`
${errorMessage}
\`\`\`

## 復旧手順

1. ローカルで再認証:
   \`\`\`bash
   node .claude/scripts/youtube/oauth-setup.js
   \`\`\`
   ブラウザで Google 認証を通すと \`.env.local\` の \`GOOGLE_OAUTH_REFRESH_TOKEN\` が更新される。

   **重要**: チャンネル選択画面で **stats47 (UCdRiwDSX1aUd0dSd7Cs08Kg)** を選ぶこと（個人ch 誤認証防止、2026-05-03 事故）。

2. GitHub Secret を更新:
   \`\`\`bash
   gh secret set GOOGLE_OAUTH_REFRESH_TOKEN -b "$(grep '^GOOGLE_OAUTH_REFRESH_TOKEN=' .env.local | cut -d= -f2-)"
   \`\`\`

3. 復旧確認:
   \`\`\`bash
   gh workflow run youtube-audit-daily.yml
   gh workflow run oauth-token-health-check.yml
   \`\`\`

4. **OAuth client が "In production" 公開されているか確認**:
   - Testing 状態だと refresh token が 7 日で auto-expire する
   - https://console.cloud.google.com/ → API & サービス → OAuth 同意画面

完了後この Issue をクローズしてください。

---
_Auto-generated by \`.claude/scripts/youtube/oauth-health-check.mjs\`_`;

  const bodyFile = `/tmp/oauth-alert-${today}.md`;
  writeFileSync(bodyFile, body);
  const out = runGh([
    "issue",
    "create",
    "--title",
    title,
    "--label",
    "oauth-alert,auto-generated",
    "--body-file",
    bodyFile,
  ]);
  if (out) log(`created alert issue: ${out.trim()}`);
}

async function main() {
  const env = loadEnv();
  const { GOOGLE_OAUTH_CLIENT_ID, GOOGLE_OAUTH_CLIENT_SECRET, GOOGLE_OAUTH_REFRESH_TOKEN } = env;

  if (!GOOGLE_OAUTH_CLIENT_ID || !GOOGLE_OAUTH_CLIENT_SECRET || !GOOGLE_OAUTH_REFRESH_TOKEN) {
    log("Missing GOOGLE_OAUTH_* env vars");
    process.exit(1);
  }

  const oauth2 = new google.auth.OAuth2(
    GOOGLE_OAUTH_CLIENT_ID,
    GOOGLE_OAUTH_CLIENT_SECRET,
  );
  oauth2.setCredentials({ refresh_token: GOOGLE_OAUTH_REFRESH_TOKEN });

  try {
    const youtube = google.youtube({ version: "v3", auth: oauth2 });
    const res = await youtube.channels.list({ mine: true, part: ["id"] });
    const channelId = res.data.items?.[0]?.id ?? "(none)";
    log(`token healthy, channel id: ${channelId}`);
    process.exit(0);
  } catch (err) {
    const message = err?.message || String(err);
    const isInvalidGrant =
      message.includes("invalid_grant") ||
      err?.response?.data?.error === "invalid_grant";

    if (isInvalidGrant) {
      log(`token expired: ${message}`);
      if (DRY_RUN) {
        log("dry-run: skipping Issue creation");
        process.exit(0);
      }
      const existing = findOpenAlertIssue();
      if (existing) {
        log(`alert issue already open: #${existing.number}, skipping`);
      } else {
        createAlertIssue(message);
      }
      process.exit(0);
    }

    log(`unexpected error: ${message}`);
    process.exit(1);
  }
}

main();
